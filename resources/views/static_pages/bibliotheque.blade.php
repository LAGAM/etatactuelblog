    <p>La bibliothèque virtuelle de côte d'ivoire est une bibliothèque en ligne. Sa particuliarité est de pouvoir y accéder partout et à tout moment.</p>
    <h1>Historique</h1>
    <hr/><br/><br/>
    <p>Le jeudi 11 août 2016, au Pôle scientifique et d’innovation de l’université Félix Houphouët-Boigny, à Bingerville, Madame le ministre de l’Enseignement Supérieur et de la Recherche Scientifique, Pr BAKAYOKO -Ly Ramata procédait au lancement de la bibliothèque virtuelle de l’enseignement supérieur et la recherche scientifique. Un projet qui vise la démocratisation de l’accès à la connaissance et au savoir, l’amélioration de la qualité de la recherche et de l’enseignement, la conservation du patrimoine scientifique pour les générations futures et le rayonnement des institutions de recherche.C’est pour capitaliser ces acquis, que la Direction Générale de l’Université Virtuelle de Côte d’Ivoire(UVCI) a procédé, le jeudi 16 février 2017, à son siège sis à l’Université Félix Houphouët-Boigny, à l’ouverture d’un atelier de sensibilisation des bibliothécaires des universités et centres de recherche. Selon le prof. Koné Tiémoman, Directeur Général de l’UVCI, la présente session porte sur le renforcement des capacités des documentalistes et autres archivistes sur le réseautage et la diffusion des informations. « Il s’agit de former les principaux animateurs de la bibliothèque virtuelle de sorte que les fonds documents se trouvant dans leur bibliothèque soient accessibles en ligne, afin qu’un plus grand nombre d’étudiants, d’enseignants et d’enseignants chercheurs puissent utiliser les ressources. A l’issue de cet atelier, des points focaux du projet TANDEM vont être désignés et ces derniers seront chargés de répéter ce type d’atelier à l’intérieur de leurs institutions respectives afin de sensibiliser une large majorité de la cible sur les modalités d’accès. Par ailleurs, dans le cadre du projet Contrat de Désendettement et de Développement(C2D), l’Institut de Recherche pour le Développement(IRD) a fait don d’un Scanner professionnel, un matériel de haute technologie, à la bibliothèque virtuelle. Pour M. Jean-Marc Hougard, le représentant de l’IRD en Côte d’Ivoire, ce geste fait partie des missions de renforcement des capacités de son institution, avant de se montrer satisfait et optimiste, vu les  bons résultats enregistrés par l’UVCI en quelques mois de fonctionnement. Il s’agit d’un Scanner Fujitsu Fi-7160, recto/ verso couleurs compatible a un ordinateur et qui fait 60 pages à la minute. Au nom de la Ministre Prof Bakayoko-Ly Ramata, le directeur général de l’UVCI a remercié l’IRD et a expliqué que cet outil permettra de numériser les documents physiques pour les mettre en ligne à la disposition des acteurs de notre système d’enseignement supérieur et de recherche. Notons qu’il s’agit d’un Scanner Fujitsu Fi-7160, recto/ verso en couleurs, compatible à un ordinateur et numérise 60 pages à la minute et que cet atelier de sensibilisation a bénéficié du soutien de la direction générale de l’Agence National du Service Universel de Télécommunication(ANSUT), représentée par son chef de projet E-éducation, M. Koné Diofowa.</p>
