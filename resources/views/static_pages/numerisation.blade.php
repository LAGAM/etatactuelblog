                        <h2 class="sc_title">Protocole d'accord avec l'Université Nangui Abrogoua (U.N.A)</h2>
                        <p>La numérisation est la conversion des informations d'un support (texte, image, audio, vidéo) ou d'un signal électrique en données numériques que des dispositifs informatiques ou d'électronique numérique pourront traiter. Les données numériques se définissent comme une suite de caractères et de nombres qui représentent des informations. On utilise parfois le terme franglais digitalisation (digit signifiant chiffre en anglais).<br/>Ainsi la numérisation de these, est la conversion en masse des documents en fichiers informatiques.</p>
                        <p>L'Université Nangui Abrogoua et l'Université Virtuelle de Côte d'Ivoire (U.V.C.I) signent un protocole d'accord.<br/>Ce protocole d’accord va permettre à ces deux institutions de fédérer leurs actions, pour un bénéfice certain de leurs apprenants, mais aussi des formateurs. 
                        Pour rappel, il faut noter qu’au début de cette année 2018, un partenariat est né entre l’UNA et l’UVCI pour la numérisation de plus 1500 mémoires environ de Master et Thèses de Doctorat du fond documentaire de la bibliothèque de l’UNA, avec l’appui d’une compagnie de téléphonie de la place. 
                        Ce pourquoi, le Président de l’UNA, Prof TANO Yao, a ajouté que pour le fonctionnement optimal de certaines des installations de son université telles que les salles multimédia (dont la troisième verra le jour bientôt), l’expertise de l’UVCI s’avère capitale.<br/>« En effet, la place du numérique dans l’enseignement, avec le système LMD est, malheureusement sous-exploitée encore dans nos institutions et l’UVCI est outillée pour nous accompagner en vue de réduire ce retard », a-t-il indiqué. 
                        C’est donc à point nommé que vient cette convention tripartite de collaboration entre l’AUF, l’UVCI et l’UNA, qui permettra la création d’espaces numériques supplémentaires qui renforceront les capacités des formateurs ainsi que apprenants de l’UNA en matière de numérique éducatif.

                        Le Prof TANO Yao a, également, fait cas, de la bibliothèque de l’UNA dont modernisation qui est pratiquement achevée grâce à un Projet d’Investissement Prioritaire (PIP), aura aussi besoin d’un accompagnement pour la rendre plus efficace, en matière d’accès à des bibliothèques virtuelles, « et nous voulons compter sur ce partenariat naturel avec l’UVCI pour y parvenir rapidement », a-t-il déclaré.

                        Par ailleurs, il reste persuadé qu’avec les bases du partenariat déjà posées entre l’UNA et l’UVCI, ils pourront co-construire d’excellents projets et réalisations, pour l’amélioration substantielle de l’enseignement supérieur en Côte d’Ivoire. 
                        Notons qu’à cette cérémonie de signature de convention, l’UVCI a été représentée par une délégation conduite par son DG KONE Tiémoman, avec la participation de responsables du CFC, CNFP et de MICROSOFT.
